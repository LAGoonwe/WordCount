    public static void count(String str){
        //将字符串转化为字符数组
        char[] chars = str.toCharArray();
        //创建一个HashMap名为hm
        Map<Character, Integer> map = new HashMap<Character, Integer>();

        //定义一个字符串c，循环遍历遍历chars数组
        for(char c : chars){
            //containsKey(c),当c不存在于hm中
            if(!map.containsKey(c)){
                map.put(c,1);
            }else{
                //否则获得c的值并且加1
                map.put(c, map.get(c)+1);
            }
        }

        List<Map.Entry<Character, Integer>> list=new ArrayList<Map.Entry<Character, Integer>>(map.entrySet());
        Collections.sort(list, (o1, o2) -> o2.getValue().compareTo(o1.getValue()));

        for (Map.Entry<Character,Integer> mapping:list){
            NumberFormat numberFormat = NumberFormat.getInstance();
            // 设置精确到小数点后2位
            numberFormat.setMaximumFractionDigits(4);
            String result = numberFormat.format((float)mapping.getValue()/(float)22370*100);
            System.out.println(mapping.getKey() + "==>这个字出现的次数为:" + mapping.getValue()+"---->"+"其中它的出现频率为："+result);
        }
}

